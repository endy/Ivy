#include "IvyInput.h"
#include "CelShadeApp.h"

///////////////////////////////////////////////////////////////////////////////////////////////////
/// main
///////////////////////////////////////////////////////////////////////////////////////////////////
int main(unsigned int argc, const char** argv)
{
    IvyApp::Configure(argc, argv);

    input();

    CelShadeApp* app = CelShadeApp::Create();

    if (app != NULL)
    {
        app->Run();
    }

    app->Destroy();
    app = NULL;

    return 0;
}

