/**************************************************************************************************
GLApp::Create
**************************************************************************************************/
GLApp* GLApp::Create(
    GLAppCreateInfo* pAppInfo)
{
    GLApp* pApp = new GLApp(pAppInfo);

    if (pApp->Init() == false)
    {
        pApp->Destroy();
        pApp = NULL;
    }

    return pApp;
}

/**************************************************************************************************
GLApp::Destroy
**************************************************************************************************/
void GLApp::Destroy()
{
    delete this;
}
